package com.shangshufang.homework.step1.knowledge9001_09;

/**
 * Java运算符-关系运算符
 */
public class Demo1 {
    public static void main(String[] args) {
        int num1 = 10;
        int num2 = 15;

        System.out.println("10 == 15：" + (num1 == num2));
        System.out.println("10 != 15：" + (num1 != num2));
        System.out.println("10 > 15：" + (num1 > num2));
        System.out.println("10 >= 15：" + (num1 >= num2));
        System.out.println("10 < 15：" + (num1 < num2));
        System.out.println("10 <= 15：" + (num1 <= num2));
    }
}